class Solution:
    def camelMatch(self, queries: List[str], pattern: str) -> List[bool]:
        trie = Trie()
        trie.insert(pattern)
        ans = []
        for query in queries:
            ans.append(trie.find(query))
        return ans


class TrieNode:
    def __init__(self):
        self.children = {}
        self.is_word = False


class Trie:
    def __init__(self):
        self.root = TrieNode()

    def insert(self, word):
        node = self.root
        for c in word:
            if c not in node.children:
                node.children[c] = TrieNode()
            node = node.children[c]
        node.is_word = True

    def find(self, word):
        node = self.root
        for c in word:
            if c not in node.children and c.isupper():
                return False
            elif c in node.children:
                node = node.children[c]
        return node.is_word

